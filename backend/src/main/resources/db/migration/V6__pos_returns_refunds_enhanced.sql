UPDATE sale_refund_lines srl
SET restock = sr.return_to_stock,
    quantity_in_base_unit = srl.quantity,
    unit_price_snapshot = sl.unit_price_snapshot,
    packaging_id = sl.packaging_id,
    packaging_name_snapshot = sl.packaging_name_snapshot,
    packaging_quantity_snapshot = sl.packaging_quantity_snapshot,
    product_id = sl.product_id
FROM sale_refunds sr,
     sale_lines sl
WHERE srl.refund_id = sr.id
  AND sl.id = srl.sale_line_id
  AND srl.restock IS NULL;

UPDATE sale_refund_lines SET restock = true WHERE restock IS NULL;
UPDATE sale_refund_lines SET quantity_in_base_unit = quantity WHERE quantity_in_base_unit IS NULL;

