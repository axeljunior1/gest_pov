CREATE TABLE IF NOT EXISTS refund_payments (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sale_refund_id bigint NOT NULL,
    original_payment_id bigint,
    cashier_id bigint NOT NULL,
    pos_session_id bigint NOT NULL,
    method character varying(30) NOT NULL,
    amount numeric(19,4) NOT NULL,
    status character varying(20) NOT NULL DEFAULT 'REFUNDED',
    refunded_at timestamp(6) with time zone NOT NULL DEFAULT now(),
    CONSTRAINT fk_refund_payments_refund FOREIGN KEY (sale_refund_id) REFERENCES sale_refunds (id),
    CONSTRAINT fk_refund_payments_original FOREIGN KEY (original_payment_id) REFERENCES payments (id),
    CONSTRAINT fk_refund_payments_cashier FOREIGN KEY (cashier_id) REFERENCES users (id),
    CONSTRAINT fk_refund_payments_session FOREIGN KEY (pos_session_id) REFERENCES pos_sessions (id),
    CONSTRAINT refund_payments_method_check CHECK (method IN ('CASH','CARD','MOBILE_MONEY','BANK_TRANSFER','OTHER')),
    CONSTRAINT refund_payments_status_check CHECK (status IN ('PENDING','REFUNDED','FAILED'))
);

CREATE INDEX IF NOT EXISTS idx_refund_payments_session ON refund_payments (pos_session_id);
CREATE INDEX IF NOT EXISTS idx_refund_payments_refund ON refund_payments (sale_refund_id);
CREATE INDEX IF NOT EXISTS idx_sale_refunds_sale ON sale_refunds (sale_id);
CREATE INDEX IF NOT EXISTS idx_sale_refunds_session ON sale_refunds (pos_session_id);
