VALUES ('Couleur', 'COULEUR', 'Attribut variante couleur', true)
ON CONFLICT (code) DO NOTHING;

INSERT INTO variant_attributes (name, code, description, is_active)
VALUES ('Taille', 'TAILLE', 'Attribut variante taille', true)
ON CONFLICT (code) DO NOTHING;

-- Migration couleur existante
INSERT INTO variant_attribute_values (attribute_id, attr_value, code, sort_order, is_active)
SELECT va.id, v.couleur, UPPER(REGEXP_REPLACE(v.couleur, '[^A-Za-z0-9]+', '', 'g')), 0, true
FROM (
    SELECT DISTINCT TRIM(couleur) AS couleur
    FROM product_variants
    WHERE couleur IS NOT NULL AND TRIM(couleur) <> ''
) v
JOIN variant_attributes va ON va.code = 'COULEUR'
ON CONFLICT (attribute_id, attr_value) DO NOTHING;

-- Migration taille existante
INSERT INTO variant_attribute_values (attribute_id, attr_value, code, sort_order, is_active)
SELECT va.id, v.taille, UPPER(REGEXP_REPLACE(v.taille, '[^A-Za-z0-9]+', '', 'g')), 0, true
FROM (
    SELECT DISTINCT TRIM(taille) AS taille
    FROM product_variants
    WHERE taille IS NOT NULL AND TRIM(taille) <> ''
) v
JOIN variant_attributes va ON va.code = 'TAILLE'
ON CONFLICT (attribute_id, attr_value) DO NOTHING;

-- Liaisons variante ↔ valeurs (couleur)
INSERT INTO product_variant_attribute_values (product_variant_id, attribute_id, attribute_value_id)
SELECT pv.id, va.id, val.id
FROM product_variants pv
JOIN variant_attributes va ON va.code = 'COULEUR'
JOIN variant_attribute_values val ON val.attribute_id = va.id AND val.attr_value = TRIM(pv.couleur)
WHERE pv.couleur IS NOT NULL AND TRIM(pv.couleur) <> ''
ON CONFLICT (product_variant_id, attribute_id) DO NOTHING;

-- Liaisons variante ↔ valeurs (taille)
INSERT INTO product_variant_attribute_values (product_variant_id, attribute_id, attribute_value_id)
SELECT pv.id, va.id, val.id
FROM product_variants pv
JOIN variant_attributes va ON va.code = 'TAILLE'
JOIN variant_attribute_values val ON val.attribute_id = va.id AND val.attr_value = TRIM(pv.taille)
WHERE pv.taille IS NOT NULL AND TRIM(pv.taille) <> ''
ON CONFLICT (product_variant_id, attribute_id) DO NOTHING;

-- Produits avec variantes : parent non vendable directement
UPDATE products p
SET is_sellable = false
WHERE EXISTS (SELECT 1 FROM product_variants pv WHERE pv.product_id = p.id);
